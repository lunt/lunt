namespace Lunt.Diagnostics
{
    /// <summary>
    /// Log levels used by the build log.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Error
        /// </summary>
        Error = 0,

        /// <summary>
        /// Warning
        /// </summary>
        Warning = 1,

        /// <summary>
        /// Information
        /// </summary>
        Information = 2,

        /// <summary>
        /// Verbose
        /// </summary>
        Verbose = 3,

        /// <summary>
        /// Debug
        /// </summary>
        Debug = 4
    }
}